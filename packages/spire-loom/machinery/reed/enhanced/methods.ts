/**
 * Method Enhancement System 🌀
 *
 * Generic method enhancement with language-specific views.
 *
 * Declares itself as the 'method' enhancement system using declareEnhancement().
 * Provides:
 * - enhanceMethod: Transform a raw method for a specific language
 * - createLanguageView: Create an idiomatic view with naming conventions
 * - createEnhancedMethod: Container with multiple language views
 *
 * @module machinery/reed/enhanced/methods
 */

import { languages as languageRegistry, getLanguageExtensionKey } from '../language/index.js';
import type {
  LanguageDefinition,
  LanguageMethod,
  NamingConventions,
  RawMethod
} from '../language/imperative.js';
import type { LanguageParam } from '../language/types.js';
import { pascalCase, camelCase, toSnakeCase } from '../../stringing.js';
import { declareEnhancement } from './enhancement.js';

