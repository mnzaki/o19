/**
 * The method enhancement system.
 *
 * Declares itself using declareEnhancement(), providing both single-item
 * enhancement and batch operations via enhanceAll().
 *
 * @example
 * ```typescript
 * // Direct use (same as enhanceMethod)
 * const enhanced = methodEnhancement(raw, 'rust');
 *
 * // Batch enhance with multiple languages
 * const enhanced = methodEnhancement.enhanceAll(methods, ['rust', 'typescript']);
 * ```
 */
export const methodEnhancement = declareEnhancement({
  name: 'method',
  enhance: enhanceMethod,
  createView: createLanguageView,
  createContainer: (raw, enhancements, defaultLangKey) =>
    createEnhancedMethod(raw, enhancements, defaultLangKey)
});
